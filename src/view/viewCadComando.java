    private void tfCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfCadastrarActionPerformed
        String comandoNomeAtual = tfNome.getText();
        String comandoSintaxeAtual = tfSintaxe.getText();
        String comandoDescricaoAtual = tfDescricao.getText();
        
        Comando comandoAtual = new Comando();
        ComandoDAO cmddao = new ComandoDAO();
        
        comandoAtual.setNome(tfNome.getText());
        comandoAtual.setDescricao(tfDescricao.getText());
        comandoAtual.setSintaxe(tfSintaxe.getText());
        
        view.viewComandos.comandos.add(comandoAtual);
        cmddao.cadastrar();
        System.out.println(view.viewComandos.comandos);
        
        tfNome.setText("");
        tfSintaxe.setText("");
        tfDescricao.setText("");
        
        
        
    }//GEN-LAST:event_tfCadastrarActionPerformed

