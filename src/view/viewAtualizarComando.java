    private void btDeletarAtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarAtActionPerformed
        Comando atualizaComando = new Comando();
        
        atualizaComando.setNome(tfNomeAt.getText());
        atualizaComando.setSintaxe(tfSintaxeAt.getText());
        atualizaComando.setDescricao(tfDescricaoAt.getText());
        
        for (Comando c:comandos){
            if(c.getNome().equals(atualizaComando.getNome())){
                comandos.remove(c);
                System.out.println(comandos);
            }
            
        }
        setVisible(false);
        new viewComandos().setVisible(true);
        
    }//GEN-LAST:event_btDeletarAtActionPerformed

    private void btAtualizarAtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarAtActionPerformed
        Comando atualizaComando = new Comando();
        
        atualizaComando.setNome(tfNomeAt.getText());
        atualizaComando.setSintaxe(tfSintaxeAt.getText());
        atualizaComando.setDescricao(tfDescricaoAt.getText());
        
        for (Comando c:comandos){
            if(c.getNome().equals(atualizaComando.getNome())){
                comandos.remove(c);
                System.out.println(comandos);

                c.setNome(atualizaComando.getNome());
                c.setSintaxe(atualizaComando.getSintaxe());
                c.setDescricao(atualizaComando.getDescricao());
                comandos.add(c);
                System.out.println(comandos);
 
            }
        }
        setVisible(false);
        new viewComandos().setVisible(true);
 
    }//GEN-LAST:event_btAtualizarAtActionPerformed

